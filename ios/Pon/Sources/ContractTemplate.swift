import Foundation

// MARK: - Contract Category

struct ContractCategory: Identifiable {
    let id: String
    let name: String
    let icon: String
    let description: String
    let templates: [ContractTemplate]
}

// MARK: - Contract Template

struct ContractTemplate: Identifiable {
    let id: String
    let name: String
    let body: String

    // Legacy compat
    var icon: String {
        ContractCategory.all.first { cat in cat.templates.contains { $0.id == id } }?.icon ?? "doc"
    }

    static var builtIn: [ContractTemplate] {
        ContractCategory.all.flatMap { $0.templates }
    }

    // MARK: - Placeholder replacement

    func fillPlaceholders(
        title: String,
        creatorName: String,
        clientName: String,
        amount: String,
        startDate: Date,
        endDate: Date?,
        paymentTerm: String = "一括払い",
        warrantyPeriod: String = "6ヶ月",
        confidentialityPeriod: String = "3年",
        jurisdiction: String = "東京地方裁判所"
    ) -> String {
        let df = DateFormatter()
        df.dateFormat = "yyyy年M月d日"
        let start = df.string(from: startDate)
        let end = endDate.map { df.string(from: $0) } ?? "期間の定めなし"

        return body
            .replacingOccurrences(of: "{甲の名前}", with: creatorName.isEmpty ? "＿＿＿＿＿＿" : creatorName)
            .replacingOccurrences(of: "{乙の名前}", with: clientName.isEmpty ? "＿＿＿＿＿＿" : clientName)
            .replacingOccurrences(of: "{契約タイトル}", with: title.isEmpty ? "＿＿＿＿＿＿" : title)
            .replacingOccurrences(of: "{契約金額}", with: amount.isEmpty ? "＿＿＿＿＿＿円" : "\(amount)円")
            .replacingOccurrences(of: "{開始日}", with: start)
            .replacingOccurrences(of: "{終了日}", with: end)
            .replacingOccurrences(of: "{支払条件}", with: paymentTerm)
            .replacingOccurrences(of: "{瑕疵担保期間}", with: warrantyPeriod)
            .replacingOccurrences(of: "{秘密保持期間}", with: confidentialityPeriod)
            .replacingOccurrences(of: "{管轄裁判所}", with: jurisdiction)
    }
}

